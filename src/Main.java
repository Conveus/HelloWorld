public class Main {
    public static void main(String[] args) {
        System.out.println("Hello world!");
        // There is a shortcut for this by typing sout
        System.out.println();
        // There are other shortcuts too, remember to look them up

        // Using print instead of println will not move the cursor to a new line
        System.out.print("First Name, ");
        System.out.println(":Last Name");

        // For loops work the same as in C++ and C#
        for(int i = 0; i <= 10; i++)
        {
            // Can print other data types
            System.out.println(i);
        }
    }
}